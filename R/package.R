#' alzio.detect: Early Diagnosis Assessment for Alzheimer's Disease
#'
#' A unified analytical toolkit for early Alzheimer's disease diagnosis assessment.
#' Provides functions for cognitive test scoring and normative comparisons,
#' biomarker interpretation, diagnostic classification, risk stratification,
#' and visualization.
#'
#' The package is organized into several modules:
#' \itemize{
#'   \item \strong{Data Import:} Helpers to read and standardize data from common AD studies.
#'   \item \strong{Cognitive Scoring:} Score calculation for standard cognitive assessments.
#'   \item \strong{Cognitive Norms:} Demographically-adjusted normative comparisons.
#'   \item \strong{Biomarker Analysis:} CSF, blood, and genetic biomarker interpretation.
#'   \item \strong{Diagnostics:} ROC analysis, classification, and diagnostic utility metrics.
#'   \item \strong{Risk Stratification:} Risk scores and progression probability.
#'   \item \strong{Visualization:} Cognitive profiles, longitudinal plots, and reports.
#' }
#'
#' @name alzio.detect-package
